/**
 * Card "shells" — the non-rules scaffolding around a card: mana / typeline / metadata / KDoc, the
 * keyword line, and the final file assembly (imports + header). All Scryfall-sourced, no TODO stubs.
 */
private val MANA = mapOf(
    "ManaCostW" to "{W}", "ManaCostU" to "{U}", "ManaCostB" to "{B}", "ManaCostR" to "{R}",
    "ManaCostG" to "{G}", "ManaCostC" to "{C}", "ManaCostX" to "{X}",
)
private val COLOR_LETTERS = listOf("W", "U", "B", "R", "G")
private val RARITY_DSL = mapOf(
    "common" to "COMMON", "uncommon" to "UNCOMMON", "rare" to "RARE",
    "mythic" to "MYTHIC", "special" to "SPECIAL", "bonus" to "BONUS",
)
private val PERMANENT_TYPES = setOf("Creature", "Artifact", "Enchantment", "Land", "Planeswalker")

// A whole-card render: generated, but still predictive — verify before relying on it.
private val COMPLETE_HEADER = listOf(
    "// === GENERATED by mtgish-tooling from the mtgish oracle IR (predictive, approximate). ===",
    "// Complete render — no manual wiring needed. Still review the rules text and add a passing",
    "// scenario test before relying on it.",
)

// An incomplete render: some structure couldn't be recovered, so it carries STRUCTURE stubs.
private val DRAFT_HEADER = listOf(
    "// TODO: GENERATED DRAFT — incomplete render, do NOT merge as-is. Finish the STRUCTURE stub(s) below.",
    "// Source: mtgish IR via the coverage bridge (predictive, approximate).",
    "// Before use: (1) finish the STRUCTURE stub(s), (2) write & pass a scenario test, (3) review the rules text.",
    "// Then move into the set's cards/ package (auto-registers via classpath scan).",
)

internal fun renderMana(cost: JsonElement?): String {
    val out = StringBuilder()
    for (s in cost.asArr ?: JsonArray(emptyList())) {
        val sym = s.strField("_ManaSymbol")
        if (sym == "ManaCostGeneric") out.append("{${s.field("args").asInt() ?: 0}}")
        else out.append(MANA[sym] ?: "{?}")
    }
    return out.toString()
}

internal fun renderTypeline(tl: JsonElement?): String {
    val supers = tl.field("Supertypes").asArr?.mapNotNull { it.asStr() } ?: emptyList()
    val types = tl.field("Cardtypes").asArr?.mapNotNull { it.asStr() } ?: emptyList()
    val subs = (tl.field("Subtypes").asArr?.mapNotNull { it.asStr() } ?: emptyList()).joinToString(" ")
    val left = (supers.joinToString(" ") + " " + types.joinToString(" ")).trim()
    return if (subs.isNotEmpty()) "$left — $subs" else left
}

internal fun ktStr(s: String): String =
    s.replace("\\", "\\\\").replace("\"", "\\\"").replace("\t", "\\t").replace("\n", "\\n")

internal fun colorIdentityDsl(meta: JsonObject?): String? {
    if (meta == null) return null
    val ci = meta["color_identity"].asArr ?: return null
    val present = ci.mapNotNull { it.asStr()?.uppercase() }.toSet()
    return COLOR_LETTERS.filter { it in present }.joinToString("")
}

internal fun metadataLines(meta: JsonObject?, indent: String = "    "): List<String> {
    if (meta == null) return listOf(indent + "metadata { rarity = Rarity.COMMON }  // no Scryfall cache for this set")
    val out = mutableListOf(
        indent + "metadata {",
        "$indent    rarity = Rarity.${RARITY_DSL[(meta.strField("rarity") ?: "").lowercase()] ?: "COMMON"}",
    )
    // Empty fields are skipped (no `collectorNumber = ""`), matching the committed golden metadata.
    meta.strField("collector_number")?.takeIf { it.isNotEmpty() }?.let { out.add("$indent    collectorNumber = \"${ktStr(it)}\"") }
    meta.strField("artist")?.takeIf { it.isNotEmpty() }?.let { out.add("$indent    artist = \"${ktStr(it)}\"") }
    meta.strField("flavor_text")?.takeIf { it.isNotEmpty() }?.let { out.add("$indent    flavorText = \"${ktStr(it.replace("*", ""))}\"") }
    meta.strField("image_uri")?.takeIf { it.isNotEmpty() }?.let { out.add("$indent    imageUri = \"${ktStr(it)}\"") }
    out.add("$indent}")
    return out
}

internal fun docCommentLines(card: JsonObject, scryfall: JsonObject?): List<String> {
    val head = mutableListOf(card["Name"].asStr() ?: "")
    val mana = renderMana(card["ManaCost"])
    if (mana.isNotEmpty()) head.add(mana)
    head.add(renderTypeline(card["Typeline"]))
    card["CardPT"].asObj?.let { pt -> head.add("${pt["Power"].asInt()}/${pt["Toughness"].asInt()}") }
    scryfall?.strField("oracle_text")?.takeIf { it.isNotEmpty() }?.let { oracle ->
        // split multi-line / multi-face text; never let "*/" close the KDoc early. An empty oracle
        // string is filtered out above, so it adds no lines.
        head.addAll(oracle.replace("*/", "* /").split("\n"))
    }
    return listOf("/**") + head.map { if (it.isNotEmpty()) " * $it" else " *" } + listOf(" */")
}

internal fun findLandwalkKeywords(node: JsonElement?, keywords: Set<String>, out: MutableSet<String>) {
    when (node) {
        is JsonObject -> {
            if (node.strField("_Rule") == "Landwalk") {
                val name = node["args"].asObj?.get("args").asStr()
                if (name != null && (name.uppercase() + "WALK") in keywords) out.add(name.uppercase() + "WALK")
            }
            node.values.forEach { findLandwalkKeywords(it, keywords, out) }
        }
        is JsonArray -> node.forEach { findLandwalkKeywords(it, keywords, out) }
        else -> {}
    }
}

internal fun keywordLines(card: JsonObject, keywords: Set<String>): Set<String> {
    val out = mutableSetOf<String>()
    findLandwalkKeywords(card["Rules"], keywords, out)
    val tags = Counter<Pair<String, String>>()
    Mtgish.extractTags(card["Rules"], tags)
    for ((disc, value) in tags.keys) {
        if (value == "Landwalk") continue
        val entry = Bridge.entry(disc, value)
        val auto = pascalToUpperSnake(value)
        if (entry is MappingEntry.Keyword) out.add(entry.tag)
        else if (auto in keywords) out.add(auto)
    }
    return out
}

internal fun isPermanent(card: JsonObject): Boolean =
    (card["Typeline"].field("Cardtypes").asArr?.mapNotNull { it.asStr() } ?: emptyList()).any { it in PERMANENT_TYPES }

internal fun assemble(body: List<String>, pkg: String, complete: Boolean): String {
    val fileHeader = if (complete) COMPLETE_HEADER else DRAFT_HEADER
    val header = fileHeader + listOf("", "package $pkg", "") + importsFor(body).map { "import $it" } + listOf("", "")
    return (header + body).joinToString("\n") + "\n"
}

private val STRING_LITERAL = Regex("\"(?:\\\\.|[^\"\\\\])*\"")
private val IDENTIFIER = Regex("[A-Za-z_][A-Za-z0-9_]*")

/**
 * Derive the import block by scanning the emitted code for SDK symbols (resolved via
 * [Registry.importFor]). Three things are skipped so nothing spurious slips in:
 *  - KDoc / comment lines and string-literal *contents* — prose (oracle text, names, flavour) can't
 *    pull in an import;
 *  - identifiers in member position (`Effects.ModifyStats`, `Zone.HAND`) — only the leftmost
 *    qualifier needs importing; a member that happens to share a name with a top-level SDK symbol
 *    must NOT be imported.
 * This is why handlers don't track imports: every symbol they emit is found here automatically.
 */
internal fun importsFor(body: List<String>): Set<String> {
    val imports = sortedSetOf<String>()
    for (line in body) {
        val trimmed = line.trimStart()
        if (trimmed.startsWith("*") || trimmed.startsWith("/*") || trimmed.startsWith("//")) continue  // KDoc / comments
        val code = line.replace(STRING_LITERAL, "\"\"")
        IDENTIFIER.findAll(code).forEach { m ->
            if (m.range.first > 0 && code[m.range.first - 1] == '.') return@forEach  // member access — qualifier carries the import
            Registry.importFor(m.value)?.let { imports.add(it) }
        }
    }
    return imports
}
