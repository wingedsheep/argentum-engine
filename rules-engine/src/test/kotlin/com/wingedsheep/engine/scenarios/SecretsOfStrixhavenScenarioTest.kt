/**
 * Behavioural scenario tests for the mtgish-autogenerated Secrets of Strixhaven (SOS) cards.
 *
 * These are the real correctness gate for the auto-generated drafts (the compile + capability gate
 * only proves they serialise): each test casts/activates the card and asserts the printed behaviour.
 */
class SecretsOfStrixhavenScenarioTest : ScenarioTestBase() {

    private val projector = StateProjector()

    init {
        test("Burrog Banemaker: {1}{B} pump gives +1/+1 until end of turn") {
            val game = scenario()
                .withPlayers()
                .withCardOnBattlefield(1, "Burrog Banemaker")
                .withLandsOnBattlefield(1, "Swamp", 2)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val banemaker = game.findPermanent("Burrog Banemaker")!!
            val ability = cardRegistry.getCard("Burrog Banemaker")!!.script.activatedAbilities[0]
            game.execute(ActivateAbility(game.player1Id, banemaker, ability.id)).error shouldBe null
            game.resolveStack()

            projector.getProjectedPower(game.state, banemaker) shouldBe 2
            projector.getProjectedToughness(game.state, banemaker) shouldBe 2
        }

        test("Noxious Newt: deathtouch and {T}: add {G}") {
            val game = scenario()
                .withPlayers()
                .withCardOnBattlefield(1, "Noxious Newt")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val newt = game.findPermanent("Noxious Newt")!!
            projector.project(game.state).hasKeyword(newt, Keyword.DEATHTOUCH).shouldBeTrue()

            val mana = cardRegistry.getCard("Noxious Newt")!!.script.activatedAbilities[0]
            game.execute(ActivateAbility(game.player1Id, newt, mana.id)).error shouldBe null
            game.state.getEntity(game.player1Id)!!.get<ManaPoolComponent>()!!.green shouldBeGreaterThanOrEqual 1
        }

        test("Chase Inspiration: target creature you control gets +0/+3 and hexproof") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Chase Inspiration")
                .withCardOnBattlefield(1, "Glory Seeker") // 2/2
                .withLandsOnBattlefield(1, "Island", 1)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val bear = game.findPermanent("Glory Seeker")!!
            game.castSpell(1, "Chase Inspiration", bear).error shouldBe null
            game.resolveStack()

            projector.getProjectedToughness(game.state, bear) shouldBe 5
            projector.project(game.state).hasKeyword(bear, Keyword.HEXPROOF).shouldBeTrue()
        }

        test("Masterful Flourish: target creature you control gets +1/+0 and indestructible") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Masterful Flourish")
                .withCardOnBattlefield(1, "Glory Seeker")
                .withLandsOnBattlefield(1, "Swamp", 1)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val bear = game.findPermanent("Glory Seeker")!!
            game.castSpell(1, "Masterful Flourish", bear).error shouldBe null
            game.resolveStack()

            projector.getProjectedPower(game.state, bear) shouldBe 3
            projector.project(game.state).hasKeyword(bear, Keyword.INDESTRUCTIBLE).shouldBeTrue()
        }

        test("Interjection: target creature gets +2/+2 and first strike") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Interjection")
                .withCardOnBattlefield(1, "Glory Seeker")
                .withLandsOnBattlefield(1, "Plains", 1)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val bear = game.findPermanent("Glory Seeker")!!
            game.castSpell(1, "Interjection", bear).error shouldBe null
            game.resolveStack()

            projector.getProjectedPower(game.state, bear) shouldBe 4
            projector.getProjectedToughness(game.state, bear) shouldBe 4
            projector.project(game.state).hasKeyword(bear, Keyword.FIRST_STRIKE).shouldBeTrue()
        }

        test("Oracle's Restoration: +1/+1, draw a card, gain 1 life") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Oracle's Restoration")
                .withCardOnBattlefield(1, "Glory Seeker")
                .withLandsOnBattlefield(1, "Forest", 1)
                .withCardInLibrary(1, "Forest")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val bear = game.findPermanent("Glory Seeker")!!
            val handBefore = game.handSize(1)
            game.castSpell(1, "Oracle's Restoration", bear).error shouldBe null
            game.resolveStack()

            projector.getProjectedPower(game.state, bear) shouldBe 3
            game.getLifeTotal(1) shouldBe 21
            // -1 for the spell leaving hand, +1 for the draw -> net same as before the cast.
            game.handSize(1) shouldBe handBefore
        }

        test("Grapple with Death: destroy target creature and gain 1 life") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Grapple with Death")
                .withCardOnBattlefield(2, "Glory Seeker")
                .withLandsOnBattlefield(1, "Swamp", 2)
                .withLandsOnBattlefield(1, "Forest", 1)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val bear = game.findPermanent("Glory Seeker")!!
            game.castSpell(1, "Grapple with Death", bear).error shouldBe null
            game.resolveStack()

            game.isOnBattlefield("Glory Seeker") shouldBe false
            game.isInGraveyard(2, "Glory Seeker").shouldBeTrue()
            game.getLifeTotal(1) shouldBe 21
        }

        test("Stand Up for Yourself: destroys a power-3+ creature, can't target a smaller one") {
            val game = scenario()
                .withPlayers()
                .withCardsInHand(1, "Stand Up for Yourself", 2)
                .withCardOnBattlefield(2, "Hill Giant")    // 3/3 — legal
                .withCardOnBattlefield(2, "Glory Seeker")  // 2/2 — illegal target
                .withLandsOnBattlefield(1, "Plains", 3)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val smallCreature = game.findPermanent("Glory Seeker")!! // 2/2
            withClue("A 2/2 is not a legal target (power < 3), so the cast is rejected") {
                game.castSpell(1, "Stand Up for Yourself", smallCreature).error shouldNotBe null
            }

            val bigCreature = game.findPermanent("Hill Giant")!! // 3/3
            game.castSpell(1, "Stand Up for Yourself", bigCreature).error shouldBe null
            game.resolveStack()
            game.isOnBattlefield("Hill Giant") shouldBe false
        }

        test("Eager Glyphmage: ETB creates a 1/1 flying Inkling token") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Eager Glyphmage")
                .withLandsOnBattlefield(1, "Plains", 4)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.castSpell(1, "Eager Glyphmage").error shouldBe null
            game.resolveStack()

            val projected = projector.project(game.state)
            val glyphmage = game.findPermanent("Eager Glyphmage")
            val token = game.state.getBattlefield(game.player1Id).firstOrNull {
                it != glyphmage && projected.isCreature(it) && projected.getPower(it) == 1
            }
            withClue("A 1/1 flying Inkling token should have been created") { (token != null).shouldBeTrue() }
            projected.hasKeyword(token!!, Keyword.FLYING).shouldBeTrue()
            projected.getPower(token) shouldBe 1
        }

        test("Shopkeeper's Bane: gains 2 life when it attacks") {
            val game = scenario()
                .withPlayers()
                .withCardOnBattlefield(1, "Shopkeeper's Bane")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.passUntilPhase(Phase.COMBAT, Step.DECLARE_ATTACKERS)
            game.declareAttackers(mapOf("Shopkeeper's Bane" to 2)).error shouldBe null
            game.resolveStack()

            game.getLifeTotal(1) shouldBe 22
        }

        test("Rearing Embermare: 4/5 with reach and haste") {
            val game = scenario()
                .withPlayers()
                .withCardOnBattlefield(1, "Rearing Embermare")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val mare = game.findPermanent("Rearing Embermare")!!
            projector.getProjectedPower(game.state, mare) shouldBe 4
            projector.getProjectedToughness(game.state, mare) shouldBe 5
            projector.project(game.state).hasKeyword(mare, Keyword.REACH).shouldBeTrue()
            projector.project(game.state).hasKeyword(mare, Keyword.HASTE).shouldBeTrue()
        }

        test("Tome Blast: deals 2 damage to any target") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Tome Blast")
                .withLandsOnBattlefield(1, "Mountain", 2)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.execute(
                CastSpell(game.player1Id, game.findCardsInHand(1, "Tome Blast").first(),
                    listOf(ChosenTarget.Player(game.player2Id)))
            ).error shouldBe null
            game.resolveStack()

            game.getLifeTotal(2) shouldBe 18
            withClue("Tome Blast has Flashback") {
                cardRegistry.getCard("Tome Blast")!!.keywordAbilities
                    .any { it.keyword == Keyword.FLASHBACK }.shouldBeTrue()
            }
        }

        test("Zealous Lorecaster: ETB returns an instant/sorcery from your graveyard to hand") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Zealous Lorecaster")
                .withCardInGraveyard(1, "Tome Blast") // a sorcery
                .withLandsOnBattlefield(1, "Mountain", 6)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.castSpell(1, "Zealous Lorecaster").error shouldBe null
            game.resolveStack() // ETB trigger -> target decision

            if (game.hasPendingDecision()) {
                val target = game.findCardsInGraveyard(1, "Tome Blast").first()
                game.selectTargets(listOf(target))
                game.resolveStack()
            }
            game.isInHand(1, "Tome Blast").shouldBeTrue()
        }

        test("Environmental Scientist: optional ETB basic-land search to hand") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Environmental Scientist")
                .withLandsOnBattlefield(1, "Forest", 2)
                .withCardInLibrary(1, "Plains")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.castSpell(1, "Environmental Scientist").error shouldBe null
            game.resolveStack()

            // "you may" -> yes, then choose the basic land.
            if (game.getPendingDecision() != null) {
                game.answerYesNo(true)
                if (game.getPendingDecision() != null) {
                    val land = game.findCardsInLibrary(1, "Plains").firstOrNull()
                    if (land != null) game.selectCards(listOf(land))
                    game.resolveStack()
                }
            }
            game.isInHand(1, "Plains").shouldBeTrue()
        }

        test("Shattered Acolyte: {1}, Sacrifice this: destroy target artifact") {
            val game = scenario()
                .withPlayers()
                .withCardOnBattlefield(1, "Shattered Acolyte")
                .withCardOnBattlefield(2, "Strixhaven Skycoach") // an Artifact — Vehicle
                .withLandsOnBattlefield(1, "Plains", 1)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val acolyte = game.findPermanent("Shattered Acolyte")!!
            val artifact = game.findPermanent("Strixhaven Skycoach")!!
            val ability = cardRegistry.getCard("Shattered Acolyte")!!.script.activatedAbilities[0]
            game.execute(
                ActivateAbility(game.player1Id, acolyte, ability.id, listOf(ChosenTarget.Permanent(artifact)))
            ).error shouldBe null
            game.resolveStack()

            game.isOnBattlefield("Strixhaven Skycoach") shouldBe false
            game.isOnBattlefield("Shattered Acolyte") shouldBe false
        }

        test("Strixhaven Skycoach: ETB optional basic-land search, has crew") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Strixhaven Skycoach")
                .withLandsOnBattlefield(1, "Plains", 3)
                .withCardInLibrary(1, "Island")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.castSpell(1, "Strixhaven Skycoach").error shouldBe null
            game.resolveStack()
            if (game.getPendingDecision() != null) {
                game.answerYesNo(true)
                if (game.getPendingDecision() != null) {
                    val land = game.findCardsInLibrary(1, "Island").firstOrNull()
                    if (land != null) game.selectCards(listOf(land))
                    game.resolveStack()
                }
            }
            game.isInHand(1, "Island").shouldBeTrue()
            cardRegistry.getCard("Strixhaven Skycoach")!!.keywordAbilities
                .any { it.keyword == Keyword.CREW }.shouldBeTrue()
        }

        test("Rapturous Moment: draw three, discard two, add {U}{U}{R}{R}{R}") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Rapturous Moment")
                .withLandsOnBattlefield(1, "Island", 3)
                .withLandsOnBattlefield(1, "Mountain", 3)
                .withCardsInHand(1, "Mountain", 2) // fodder to discard
                .withCardInLibrary(1, "Island")
                .withCardInLibrary(1, "Island")
                .withCardInLibrary(1, "Island")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.castSpell(1, "Rapturous Moment").error shouldBe null
            game.resolveStack()
            // Discard two as required.
            if (game.getPendingDecision() != null) {
                val toDiscard = game.findCardsInHand(1, "Mountain").take(2)
                game.selectCards(toDiscard)
                game.resolveStack()
            }
            val pool = game.state.getEntity(game.player1Id)!!.get<ManaPoolComponent>()!!
            pool.blue shouldBeGreaterThanOrEqual 2
            pool.red shouldBeGreaterThanOrEqual 3
        }

        test("Traumatic Critique: X damage to any target, draw two then discard one") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Traumatic Critique")
                .withLandsOnBattlefield(1, "Island", 1)
                .withLandsOnBattlefield(1, "Mountain", 4)
                .withCardInLibrary(1, "Island")
                .withCardInLibrary(1, "Island")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.execute(
                CastSpell(game.player1Id, game.findCardsInHand(1, "Traumatic Critique").first(),
                    listOf(ChosenTarget.Player(game.player2Id)), xValue = 3)
            ).error shouldBe null
            game.resolveStack()
            if (game.getPendingDecision() != null) {
                val toDiscard = game.state.getHand(game.player1Id).take(1)
                game.selectCards(toDiscard)
                game.resolveStack()
            }
            game.getLifeTotal(2) shouldBe 17
        }

        test("Cost of Brilliance: player draws and loses life, optional creature gets counter") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Cost of Brilliance")
                .withCardOnBattlefield(1, "Glory Seeker")
                .withLandsOnBattlefield(1, "Swamp", 3)
                .withCardInLibrary(2, "Island")
                .withCardInLibrary(2, "Island")
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            val creature = game.findPermanent("Glory Seeker")!!
            game.execute(
                CastSpell(
                    game.player1Id,
                    game.findCardsInHand(1, "Cost of Brilliance").first(),
                    listOf(ChosenTarget.Player(game.player2Id), ChosenTarget.Permanent(creature)),
                )
            ).error shouldBe null
            game.resolveStack()

            game.getLifeTotal(2) shouldBe 18
            game.handSize(2) shouldBe 2
            plusOnePlusOne(game, creature) shouldBe 1
            projector.getProjectedPower(game.state, creature) shouldBe 3
        }

        test("Pterafractyl: X counters are inherited as it enters and ETB gains life") {
            val game = scenario()
                .withPlayers()
                .withCardInHand(1, "Pterafractyl")
                .withLandsOnBattlefield(1, "Forest", 4)
                .withLandsOnBattlefield(1, "Island", 1)
                .withActivePlayer(1)
                .inPhase(Phase.PRECOMBAT_MAIN, Step.PRECOMBAT_MAIN)
                .build()

            game.execute(
                CastSpell(
                    game.player1Id,
                    game.findCardsInHand(1, "Pterafractyl").first(),
                    xValue = 3,
                )
            ).error shouldBe null
            game.resolveStack()

            val pterafractyl = game.findPermanent("Pterafractyl")!!
            plusOnePlusOne(game, pterafractyl) shouldBe 3
            projector.getProjectedPower(game.state, pterafractyl) shouldBe 4
            projector.getProjectedToughness(game.state, pterafractyl) shouldBe 3
            game.getLifeTotal(1) shouldBe 22
        }
    }

    private fun plusOnePlusOne(game: TestGame, id: EntityId): Int =
        game.state.getEntity(id)?.get<CountersComponent>()?.getCount(CounterType.PLUS_ONE_PLUS_ONE) ?: 0
}
